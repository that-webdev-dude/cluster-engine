class Level extends TileMap {
  constructor(width, height) {
    const texture = new Texture(charactersImageURL);
    const tileSize = 32;
    const mapW = (width || 832) / tileSize;
    const mapH = (height || 640) / tileSize;
    const tiles = levelMap.map((symbol, index) => {
      const tile = {};
      switch (symbol) {
        case "#": // block tile
          tile.walkable = false;
          tile.x = math.rand(0, 3);
          tile.y = 4;
          break;
        default: // walkable tile
          tile.walkable = true;
          tile.x = 0;
          tile.y = 5;
          break;
      }
      return tile;
    });

    super(tiles, mapW, mapH, tileSize, tileSize, texture);
  }
}

export default Level;
