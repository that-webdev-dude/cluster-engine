class Level extends TileMap {
  constructor(width, height) {
    const texture = new Texture(charactersImageURL);
    const tileSize = 32;
    const mapW = (width || 832) / tileSize;
    const mapH = (height || 640) / tileSize;
    const tiles = levelMap.map((symbol, index) => {
      const tile = {};
      switch (symbol) {
        case "#": // block tile
          tile.walkable = false;
          tile.bridge = false;
          tile.cloud = false;
          tile.x = math.rand(0, 3);
          tile.y = 4;
          break;
        case "C": // cloud tile
          tile.walkable = true;
          tile.bridge = false;
          tile.cloud = true;
          tile.x = 1;
          tile.y = 5;
          break;
        case "B": // bridge tile
          tile.walkable = false;
          tile.counter = 0;
          tile.bridge = true;
          tile.cloud = false;
          tile.x = math.rand(0, 3);
          tile.y = 4;
          // tile.x = 2;
          // tile.y = 5;
          break;
        default: // walkable tile
          tile.walkable = true;
          tile.bridge = false;
          tile.cloud = false;
          tile.x = 0;
          tile.y = 5;
          break;
      }
      return tile;
    });

    super(tiles, mapW, mapH, tileSize, tileSize, texture);
  }

  makeDisappearingTile(tile, duration = 1) {
    let { frame } = tile;
    if (!frame.bridge || frame.counter > 0) return;

    frame.counter = duration;
    const update = tile.update;

    tile.update = function (dt, t) {
      update.call(this, dt, t);
      frame.counter -= dt;
      if (frame.counter <= duration * 0.33) {
        frame.x = 2;
        frame.y = 5;
      }

      if (frame.counter <= 0) {
        frame.walkable = true;
        frame.bridge = false;
        frame.cloud = false;
        frame.x = 0;
        frame.y = 5;

        this.update = update;
      }
    };
  }
}

export default Level;
